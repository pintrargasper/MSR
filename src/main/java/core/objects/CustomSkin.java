package core.objects;

public class CustomSkin {

    private Long id;
    private String name;
    private String picture;
    private float speed;
    private int width;
    private int height;
    private String soundEffect;
    private float price;

    public CustomSkin() {}
    public Long getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public String getPicture() {
        return picture;
    }
    public float getSpeed() {
        return speed;
    }
    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }
    public String getSoundEffect() {
        return soundEffect;
    }
    public float getPrice() {
        return price;
    }
}